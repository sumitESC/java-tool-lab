//Write a Java program to declare and initialize an integer array with 5 elements and display all the elements using a for loop
import java.util.*;
{
    class Q1
    {
    public static void main(String[] args) {
        int[] arr = {10, 20, 30, 40, 50}; 
        
        System.out.println("Elements of the array:");
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]); 
        }
    }
    }
}